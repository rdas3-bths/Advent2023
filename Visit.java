public class Visit {
    private int r;
    private int c;
    private char d;

    public Visit(int r, int c, char d) {
        this.r = r;
        this.c = c;
        this.d = d;
    }

    public int getR() {
        return r;
    }

    public int getC() {
        return c;
    }

    public char getD() {
        return d;
    }
}
